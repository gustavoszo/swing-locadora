    private void jButtonDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeletarActionPerformed
        if (this.cliente == null) {
            JOptionPane.showMessageDialog(null, "Selecione um cliente para deletar");
            return;
        }
        
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente apagar o cliente?", "Confirma√ß√£o", JOptionPane.YES_NO_OPTION);
        if (resposta != JOptionPane.YES_OPTION) return;
        
        try {
            this.clienteController.deletar(cliente);
            this.clienteNull();
            JOptionPane.showMessageDialog(null, "Cliente deletado com sucesso");
        } catch(DbException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButtonDeletarActionPerformed

