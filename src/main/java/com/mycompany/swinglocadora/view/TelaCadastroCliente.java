    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if (this.cliente == null) this.cliente = new Cliente();
        jLabelErrorNome.setText("");
        jLabelErrorCpf.setText("");
        jLabelErrorEmail.setText("");
        jLabelErrorEndereco.setText("");
        jLabelErrorDtNascimento.setText("");
        
        SimpleDateFormat sdf = new SimpleDateFormat("dd/mm/yyyy");
        Date data;
        
        try {
            data = sdf.parse(jFormattedTextFieldDtNascimento.getText());
        } catch (ParseException ex) {
            data = null;
        }
       
        try {
            Integer id = this.cliente.getId_cliente();
            this.cliente.setNome(jTextFieldNome.getText());
            this.cliente.setCpf(jTextFieldCpf.getText());
            this.cliente.setEmail(jTextFieldEmail.getText());
            this.cliente.setEndereco(jTextFieldEndereco.getText());
            this.cliente.setDtNascimento(data);
            clienteController.save(this.cliente);
            
            if (id == null) {
                JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso");
            } else {
                JOptionPane.showMessageDialog(null, "Cliente atualizado com sucesso");
            }
            this.jButtonLimparActionPerformed(evt);
            this.cliente = null;
          
        } catch (ValidationException e) {
            Set<String> fields = e.getErrors().keySet();
		
            if (fields.contains("nome")) {
		jLabelErrorNome.setText(e.getErrors().get("nome"));
            }
            
            if (fields.contains("cpf")) {
		jLabelErrorCpf.setText(e.getErrors().get("cpf"));
            }
            
            if (fields.contains("email")) {
		jLabelErrorEmail.setText(e.getErrors().get("email"));
            }
            
            if (fields.contains("endereco")) {
		jLabelErrorEndereco.setText(e.getErrors().get("endereco"));
            }
            
            if (fields.contains("dtNascimento")) {
		jLabelErrorDtNascimento.setText(e.getErrors().get("dtNascimento"));
            }
        } catch (DbException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

