    private void jButtonDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeletarActionPerformed
        if (this.ator == null) {
            JOptionPane.showMessageDialog(null, "Selecione um ator para deletar");
            return;
        }
        
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente apagar o ator?", "Confirmação", JOptionPane.YES_NO_OPTION);
        if (resposta != JOptionPane.YES_OPTION) return;
        
        try {
            this.atorController.delete(this.ator);
            this.atorNull();
            JOptionPane.showMessageDialog(null, "Ator deletado com sucesso");
        } catch(DbException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButtonDeletarActionPerformed

