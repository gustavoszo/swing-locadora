    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if (this.ator == null) this.ator = new Ator();
        String nacionalidade = jComboBoxNacionalidade.getSelectedItem().toString();
        
        this.ator.setNome(jTextFieldNome.getText());
        this.ator.setNacionalidade(nacionalidade);
        try {
            Integer id = this.ator.getId_ator();
            atorController.save(this.ator);
            if (id == null) {
                JOptionPane.showMessageDialog(null, "Ator cadastrado com sucesso");           
            } else {
                JOptionPane.showMessageDialog(null, "Ator atualizado com sucesso");           
            }
            this.jButtonLimparActionPerformed(evt);
            this.ator = new Ator();
         
        } catch (ValidationException e) {
            Set<String> fields = e.getErrors().keySet();
		
            if (fields.contains("nome")) {
		jLabelErrorNome.setText(e.getErrors().get("nome"));
            }
        } catch(DbException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

